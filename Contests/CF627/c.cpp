int bfs(string s){
    int ans = -1;
    int n = s.size();
    int cur = n + 1;
    for(int i = n; i >= 0; i--){
        if(i == 0) ans = max(cur - 0, ans);
        else if(s[i-1] == 'R') ans = max(cur - i, ans), cur = i;
    }
    return ans;
}

